    private void buttonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonMousePressed
        String artic = articulo_desc.getText();
        
        String dn = dni.getText();
        int intdn = 0;
        
        
        Elementos mElemento = (Elementos) comboBoxElementos.getSelectedItem();
        int id_elem = mElemento.getId();
        String elem = mElemento.getElemento();
        
        // Conditions
        if(dn.equals("") || artic.equals("Ingrese el estado del elemento a devolver")){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            articulo_desc.requestFocus();
        }
        else{
            try
            {
                intdn = Integer.parseInt(dni.getText());
                
                if(intdn <= 0){
                    javax.swing.JOptionPane.showMessageDialog(this, "El dni del usuario debe ser mayor a 0. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                     dni.requestFocus();
                }
                
                try {
                    // Verificamos el usuario
                    boolean pase = Inter.UserExist(intdn);
                    if(!pase){
                        javax.swing.JOptionPane.showMessageDialog(this, "No existe ning√∫n usuario con ese dni. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                         dni.requestFocus();
                    }
                    // Verificamos el elemento
                    //else if (!Inter.Articulo(artic))
                    //{
                    //    javax.swing.JOptionPane.showMessageDialog(this, "No existe ning√∫n libro con esa ID. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    //    articulo_id.requestFocus();
                    //}
                    else if(!Inter.CheckLending(intdn, id_elem)){
                       javax.swing.JOptionPane.showMessageDialog(this, "No se ha podido encontrar el pr√©stamo correspiendote a los datos ingresados. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                        articulo_desc.requestFocus();
                    }
                    else{
                        // DEVOLVEMOS
                        Inter.Devolutions(intdn, id_elem);
                        dni.setText("");
                        articulo_desc.setText("");
                    }
                    
                    
                    Statement stm = reg.createStatement();
                    stm.executeUpdate("UPDATE `articulos` SET  `description` = '"+ artic +"' WHERE `id` = '"+ id_elem +"';");
                    stm.close();
                    
                    
                } catch (SQLException ex) {
                    Logger.getLogger(Returns.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }catch(Exception ex){
                javax.swing.JOptionPane.showMessageDialog(this, "El Dni del usuario debe ser un n√∫mero entero. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                 dni.requestFocus();
            }            
        }   
    }//GEN-LAST:event_buttonMousePressed
    
    public void Devolutions(int dn, String id_elem, int artic) throws SQLException, ParseException{
        Statement stm = reg.createStatement();
        System.out.println(artic);
      
           
        stm.executeUpdate("DELETE FROM `lendings` WHERE `articulo_id` = '"+ id_elem +"' AND `user_id` = '"+ dn +"'");
        stm.executeUpdate("UPDATE `articulos` SET `available` = available+1, `description` = '"+ artic +"' WHERE `id` = '"+ id_elem +"';");
        
        javax.swing.JOptionPane.showMessageDialog(this, "¬°Devoluci√≥n realizada correctamente! \n", "HECHO", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        
        stm.close();

    }
    
    
